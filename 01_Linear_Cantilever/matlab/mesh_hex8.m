%% =========================================================
% Generate 3D Hexahedral Mesh
%% =========================================================

function [N,T] = mesh_hex8(Lx,Ly,Lz,nx,ny,nz)

%% =========================================================
% Node coordinates
%% =========================================================

x = linspace(0,Lx,nx+1);
y = linspace(0,Ly,ny+1);
z = linspace(0,Lz,nz+1);

[X,Y,Z] = meshgrid(x,y,z);

N = [X(:), Y(:), Z(:)];

%% =========================================================
% Element connectivity
%% =========================================================

T = [];

%% Nodes per layer
nxy = (nx+1)*(ny+1);

for k = 1:nz

    for i = 1:nx

        for j = 1:ny

            %% =============================================
            % Bottom face nodes
            %% =============================================

            n1 = (k-1)*nxy + (i-1)*(ny+1) + j;
            n2 = n1 + 1;
            n4 = n1 + (ny+1);
            n3 = n4 + 1;

            %% =============================================
            % Top face nodes
            %% =============================================

            n5 = n1 + nxy;
            n6 = n2 + nxy;
            n7 = n3 + nxy;
            n8 = n4 + nxy;

            %% =============================================
            % Hex8 element
            %% =============================================

            T = [
                T
                n1 n2 n3 n4 n5 n6 n7 n8
            ];

        end
    end
end

end
